export interface Book {
  id: string;
  title: string;
  author: string;
  cover: string;
  status: '在读' | '已读' | '想读';
  rating?: number;
  highlights: number;
  notes: number;
  moodTags: string[];
  themeTags: string[];
  progress?: number;
}

export interface User {
  id: string;
  name: string;
  avatar: string;
  bio: string;
  identityTags: string[];
  booksRead: number;
  highlights: number;
  followers: number;
}

export interface Story {
  id: string;
  user: User;
  book: Book;
  type: '短评' | '摘录' | '读后感';
  content: string;
  quote?: string;
  page?: number;
  likes: number;
  comments: number;
  bookmarks: number;
  createdAt: string;
  liked?: boolean;
  bookmarked?: boolean;
}

export interface Message {
  id: string;
  user: User;
  lastMessage: string;
  time: string;
  unread: number;
  sharedBook?: string;
}

export interface ChatMessage {
  id: string;
  sender: 'me' | 'other';
  content: string;
  time: string;
  type?: 'text' | 'icebreaker' | 'book-intro';
  bookTitle?: string;
}

export interface ReadingTrace {
  id: string;
  type: '划线' | '批注' | '页码标记';
  content: string;
  page: number;
  note?: string;
  createdAt: string;
  mood?: string;
}

